# - Memory

L = [1,2,3]
print("ID of L:", id(L))
print("ID of L[0]:", id(L[0]))
print("ID of L[1]:", id(L[1]))
print("ID of L[2]:", id(L[2]))
print("ID of 1:", id(1))
